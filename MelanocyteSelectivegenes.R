#Function to select Skin Specific genes from GTEX Data
# Select Max Exprs in Skin  vs max expression in any other cell type
SkinSelective <- function(brain=TRUE){
  if(!exists("Gtex")) {
    load("./Data/MembraneDataAll.RData")
  }
  
  dfSkin    <-data.frame(Gtex[,3:55])
  SkinIndex <- grep("Skin", names(dfSkin))
  BrainIndex <- grep("Brain|Nerve", names(dfSkin))
    SkinMax   <- apply(dfSkin[,SkinIndex], 1, max)
    OthersMax <- apply(dfSkin[,c(-1, -SkinIndex)], 1, max)
    if (brain==TRUE){OthersMax <- apply(dfSkin[,c(-1, -SkinIndex,-BrainIndex)], 1, max)}
    SkinSelFC <- ((SkinMax+1)/(OthersMax+1))
  DFSkin    <- data.frame(Gtex$Description, SkinMax, OthersMax,  SkinSelFC)
    DFSkin   <- DFSkin[order(DFSkin$SkinSelFC, decreasing = T),]
  return(DFSkin)
}

