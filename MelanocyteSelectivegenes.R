SelectMelocyteSkinSpecificGenes <- function(Melfc=2, SkinFC=1){
  require(data.table) ## 1.9.2
  MelaSkinSpecific_Output <- MelanocyteSelective()
  MelaSkinSpecific_Output <- MelaSkinSpecific_Output[MelaSkinSpecific_Output$MelanocyteMax>5,]
    MelaSkinSpecific_Output  <- MelaSkinSpecific_Output[MelaSkinSpecific_Output$MelaSelFC >Melfc,]
    MelaSkinSpecific_Output$Gene <- gsub("p[0-9]+@", "", MelaSkinSpecific_Output$df.X)
    group <- as.data.table(MelaSkinSpecific_Output)
  #If you want to keep all the entries corresponding to max values of pt within each group:
  #Select transcript for gene with the highest expression
    group <- unique(group[group[, .I[MelaSelFC == max(MelaSelFC)], by=Gene]$V1])
    
    DFSkin <- SkinSelective() #Gtex
    DFSkin <- DFSkin[DFSkin$SkinSelFC>SkinFC,]
    
    #Merge Melanocyte specific and Skin specific
    Output <- merge(group, DFSkin, by.x="Gene", by.y="Gtex.Description")
      Output <- unique(Output)
      Output <- Output[order(Output$MelaSelFC, decreasing = TRUE)]
      SkinProtien <- SkinAtlasExpress(gene = Output$Gene)

  return(list(Output, SkinProtien))
}


